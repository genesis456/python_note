#   相比上面代码的优点：如果前面的条件满足，后续条件不再判断
season = input("请输入季度：")
if season == "春":
    print("１月～３月")

elif  season == "夏":
    print("４月～６月")

elif season == "秋":
    print("７月～９月")

elif season == "冬":
    print("１０月～１２月")

